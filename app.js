const langToggle = document.querySelector("#langToggle");
const langElements = document.querySelectorAll("[data-i18n]");
const metaDescription = document.querySelector('meta[name="description"]');
const ogTitle = document.querySelector('meta[property="og:title"]');
const ogDescription = document.querySelector('meta[property="og:description"]');
const ogLocale = document.querySelector('meta[property="og:locale"]');
const savedLanguage = localStorage.getItem("portfolio-language");
const browserLanguage = navigator.language.toLowerCase().startsWith("pt") ? "pt" : "en";

function setLanguage(language) {
  const selected = translations[language] ? language : "pt";

  langElements.forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[selected][key]) {
      if (key === "heroName") {
        element.innerHTML = translations[selected][key].replace("\n", "<br>");
      } else {
        element.textContent = translations[selected][key];
      }
    }
  });

  document.documentElement.lang = selected === "pt" ? "pt-BR" : "en";
  document.title = translations[selected].pageTitle;
  metaDescription?.setAttribute("content", translations[selected].pageDescription);
  ogTitle?.setAttribute("content", translations[selected].pageTitle);
  ogDescription?.setAttribute("content", translations[selected].pageDescription);
  ogLocale?.setAttribute("content", selected === "pt" ? "pt_BR" : "en_US");
  langToggle?.setAttribute(
    "aria-label",
    selected === "pt" ? "Mudar idioma para inglês" : "Switch language to Portuguese"
  );

  // Troca o link do currículo conforme o idioma
  const cvLink = document.getElementById("cvDownload");
  if (cvLink) {
    cvLink.href = selected === "pt" ? "./curriculo-pt.pdf" : "./curriculo-en.pdf";
  }

  localStorage.setItem("portfolio-language", selected);
}

langToggle.addEventListener("click", () => {
  const current = localStorage.getItem("portfolio-language") || browserLanguage;
  setLanguage(current === "pt" ? "en" : "pt");
});

setLanguage(savedLanguage || browserLanguage);

// --- Scroll Animations (Intersection Observer) ---
document.addEventListener("DOMContentLoaded", () => {
  const observerOptions = {
    root: null,
    rootMargin: "0px",
    threshold: 0.15
  };

  const observer = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target); // Anima apenas uma vez
      }
    });
  }, observerOptions);

  const animatedSections = document.querySelectorAll(".section-animate");
  animatedSections.forEach(section => {
    observer.observe(section);
  });

  // --- Scroll Spy (Active Nav Link) ---
  const navLinks = document.querySelectorAll(".topnav a[href^='#']");
  const sections = document.querySelectorAll("section[id]");

  function updateActiveNav() {
    const scrollY = window.scrollY + 200;

    sections.forEach(section => {
      const top = section.offsetTop;
      const height = section.offsetHeight;
      const id = section.getAttribute("id");

      if (scrollY >= top && scrollY < top + height) {
        navLinks.forEach(link => {
          link.classList.remove("active");
          if (link.getAttribute("href") === `#${id}`) {
            link.classList.add("active");
          }
        });
      }
    });
  }

  window.addEventListener("scroll", updateActiveNav, { passive: true });

  // --- Back to Top Button ---
  const backToTop = document.getElementById("backToTop");
  if (backToTop) {
    window.addEventListener("scroll", () => {
      if (window.scrollY > 600) {
        backToTop.classList.add("visible");
      } else {
        backToTop.classList.remove("visible");
      }
    }, { passive: true });

    backToTop.addEventListener("click", () => {
      window.scrollTo({ top: 0, behavior: "smooth" });
    });
  }

  // --- Typewriter Effect ---
  const heroTitle = document.querySelector(".hero h1.typewriter");
  if (heroTitle) {
    const fullHTML = heroTitle.innerHTML;
    heroTitle.innerHTML = "";
    heroTitle.style.visibility = "visible";

    // Flatten text from HTML (preserving <br>)
    const parts = fullHTML.split("<br>");
    let charIndex = 0;
    let partIndex = 0;
    let currentText = "";

    function typeChar() {
      if (partIndex < parts.length) {
        const part = parts[partIndex];
        if (charIndex < part.length) {
          currentText += part[charIndex];
          heroTitle.innerHTML = currentText + (partIndex < parts.length - 1 ? "" : "");
          charIndex++;
          setTimeout(typeChar, 45);
        } else {
          if (partIndex < parts.length - 1) {
            currentText += "<br>";
            heroTitle.innerHTML = currentText;
          }
          partIndex++;
          charIndex = 0;
          setTimeout(typeChar, 100);
        }
      } else {
        heroTitle.classList.add("done");
      }
    }

    // Start typing after a small delay
    setTimeout(typeChar, 600);
  }
});
