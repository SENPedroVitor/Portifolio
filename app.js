const langToggle = document.querySelector("#langToggle");
const langElements = document.querySelectorAll("[data-i18n]");
const metaDescription = document.querySelector('meta[name="description"]');
const ogTitle = document.querySelector('meta[property="og:title"]');
const ogDescription = document.querySelector('meta[property="og:description"]');
const ogLocale = document.querySelector('meta[property="og:locale"]');
const savedLanguage = localStorage.getItem("portfolio-language");
const browserLanguage = navigator.language.toLowerCase().startsWith("pt") ? "pt" : "en";

function setLanguage(language) {
  const selected = translations[language] ? language : "pt";

  langElements.forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[selected][key]) {
      if (key === "heroName") {
        element.innerHTML = translations[selected][key].replace("\n", "<br>");
      } else {
        element.textContent = translations[selected][key];
      }
    }
  });

  document.documentElement.lang = selected === "pt" ? "pt-BR" : "en";
  document.title = translations[selected].pageTitle;
  metaDescription?.setAttribute("content", translations[selected].pageDescription);
  ogTitle?.setAttribute("content", translations[selected].pageTitle);
  ogDescription?.setAttribute("content", translations[selected].pageDescription);
  ogLocale?.setAttribute("content", selected === "pt" ? "pt_BR" : "en_US");
  langToggle?.setAttribute(
    "aria-label",
    selected === "pt" ? "Mudar idioma para inglês" : "Switch language to Portuguese"
  );

  // Troca o link do currículo conforme o idioma
  const cvLink = document.getElementById("cvDownload");
  if (cvLink) {
    cvLink.href = selected === "pt" ? "./curriculo-pt.pdf" : "./curriculo-en.pdf";
  }

  localStorage.setItem("portfolio-language", selected);
}

langToggle.addEventListener("click", () => {
  const current = localStorage.getItem("portfolio-language") || browserLanguage;
  setLanguage(current === "pt" ? "en" : "pt");
});

setLanguage(savedLanguage || browserLanguage);

// --- Theme Toggle (Dark/Light) ---
const themeToggle = document.getElementById("themeToggle");
const themeIcon = document.getElementById("themeIcon");
const body = document.body;

function setTheme(dark) {
  if (dark) {
    body.classList.add("dark");
    themeIcon.className = "ph ph-sun";
    document.querySelector('meta[name="theme-color"]')?.setAttribute("content", "#1a1a1c");
  } else {
    body.classList.remove("dark");
    themeIcon.className = "ph ph-moon";
    document.querySelector('meta[name="theme-color"]')?.setAttribute("content", "#faf9f7");
  }
  localStorage.setItem("portfolio-theme", dark ? "dark" : "light");
}

// Check saved preference or system preference
const savedTheme = localStorage.getItem("portfolio-theme");
const prefersDark = window.matchMedia("(prefers-color-scheme: dark)").matches;

if (savedTheme === "dark" || (!savedTheme && prefersDark)) {
  setTheme(true);
}

themeToggle?.addEventListener("click", () => {
  themeToggle.classList.add("theme-spinning");
  setTimeout(() => {
    themeToggle.classList.remove("theme-spinning");
  }, 500);
  setTheme(!body.classList.contains("dark"));
});

